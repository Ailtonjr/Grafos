
package br.univali.grafos.modelo;


public class Buraco {
    private int x;
    private int y;

    public Buraco(int x, int y) {
        this.x = x;
        this.y = y;
    }
    

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
    
    
}
